import math
angulo = float(input("Dige um 창ngulo que vc deseja: "))
seno = math.sin(math.radians(angulo))
print("O 찾ngulo de {} tem o SENO de {:.2f}.".format(angulo, seno))
cosseno = math.cos(math.radians(angulo))
print("O 찾ngulo de {} tem o COSSENO de {:.2f}.".format(angulo, cosseno))
tangente = math.tan(math.radians(angulo))
print("O 창ngulo de {} tem a TANGENTE de {:.2f}".format(angulo, tangente))

